// Синтаксический сахар — это способ написания кода, который делает его более кратким или понятным,
// но при этом не меняет его основного значения или функциональности.

// Это как "сладкий обман" для программиста: код выглядит проще, чем он есть на самом деле


// 1. Arrow Functions:

function add(a, b) {
  return a + b;
}

// Синтаксический сахар с использованием стрелочной функции
const add = (a, b) => a + b;